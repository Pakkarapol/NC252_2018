
	public class Dic{
		private String Conver;
		private String Mean;
		


	public Dic(String Conver,String Mean) {
		this.Conver = Conver;
		this.Mean = Mean;
	}

	public String getConver() {
		return Conver;
	}

	public void setConver(String conver) {
		Conver = conver;
	}
	
	public String getMean() {
		return Mean;
	}

	public void setMean(String mean) {
		Mean = mean;
	}
	}

